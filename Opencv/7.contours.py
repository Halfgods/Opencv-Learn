import cv2 as cv
frame = cv.imread('pictures/cat2.jpg')
img = cv.resize(frame , (500,500),interpolation=cv.INTER_CUBIC)
ret , tresh = cv.threshold(img )

blur = cv.GaussianBlur(img , (3,3),cv.BORDER_DEFAULT)

canny = cv.Canny(blur , 150 , 175)

contours , hierachies = cv.findContours(canny , cv.RETR_TREE , cv.CHAIN_APPROX_SIMPLE)

cv.imshow('Test' , canny)

print(len(contours))

cv.waitKey(0)








































































































































































